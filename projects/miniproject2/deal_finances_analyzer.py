@dataclass
class SynergyProfile:
    cost_synergies_mm: Optional[float] = None
    cost_synergies_note: str = ""
    revenue_synergies_mm: Optional[float] = None
    revenue_synergies_note: str = ""
    revenue_synergy_inflation: str = "UNKNOWN"  # LOW / MODERATE / HIGH


@dataclass
class IntegrationCosts:
    total_mm: Optional[float] = None
    note: str = ""


@dataclass
class FinancialWording:
    """Captures specific financial language used in deal communications and what it signals."""
    term: str
    used_in: str          # Where this term appeared (press release, analyst note, etc.)
    literal_meaning: str  # What the term technically means
    subtext: str          # What the term is often used to obscure or signal
    red_flag: bool = False


@dataclass
class ComparableTransaction:
    name: str
    year: int
    ev_revenue: Optional[float] = None
    ev_ebitda: Optional[float] = None
    note: str = ""


@dataclass
class Deal:
    name: str
    acquirer: str
    target: str
    sector: str
    announced: str
    closed: str
    deal_value_mm: float
    enterprise_value_mm: float
    consideration: str

    # Revenue
    revenue_mm: float
    revenue_label: str
    alt_revenue_mm: Optional[float] = None
    alt_revenue_label: str = ""

    # EBITDA — REPORTED ONLY (not adjusted)
    reported_ebitda_mm: Optional[float] = None
    ebitda_source_note: str = ""
    ebitda_definition_warning: str = ""

    # Adjusted EBITDA (shown for disclosure but NOT used in analysis)
    adjusted_ebitda_mm: Optional[float] = None
    adjusted_ebitda_note: str = ""

    # Net Income
    net_income_mm: Optional[float] = None

    # Computed multiples
    synergies: SynergyProfile = field(default_factory=SynergyProfile)
    integration_costs: IntegrationCosts = field(default_factory=IntegrationCosts)
    comparable_transactions: list = field(default_factory=list)
    risks: list = field(default_factory=list)
    key_sources: list = field(default_factory=list)
    financial_wording: list = field(default_factory=list)

    @property
    def ev_revenue(self) -> Optional[float]:
        if self.revenue_mm and self.revenue_mm > 0:
            return self.enterprise_value_mm / self.revenue_mm
        return None

    @property
    def ev_alt_revenue(self) -> Optional[float]:
        if self.alt_revenue_mm and self.alt_revenue_mm > 0:
            return self.enterprise_value_mm / self.alt_revenue_mm
        return None

    @property
    def ev_reported_ebitda(self) -> Optional[float]:
        if self.reported_ebitda_mm and self.reported_ebitda_mm > 0:
            return self.enterprise_value_mm / self.reported_ebitda_mm
        return None

    @property
    def reported_ebitda_margin(self) -> Optional[float]:
        if self.reported_ebitda_mm and self.revenue_mm and self.revenue_mm > 0:
            return (self.reported_ebitda_mm / self.revenue_mm) * 100
        return None


