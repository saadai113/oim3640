def plot_comparison(deal_a: Deal, deal_b: Deal):
    """Generate bar charts comparing the two deals."""
    if not HAS_MATPLOTLIB:
        print("matplotlib not installed — skipping charts.")
        return

    labels = [deal_a.name.split("/")[0].strip(), deal_b.name.split("+")[0].strip()]
    colors = ["#4C9BE8", "#E87B4C"]

    fig, axes = plt.subplots(2, 2, figsize=(13, 9))
    fig.suptitle("Deal Comparison: VMG/Downtown  vs.  Playlist+EGYM", fontsize=14, fontweight="bold")

    # --- Chart 1: Enterprise Value ---
    ax = axes[0, 0]
    evs = [deal_a.enterprise_value_mm / 1000, deal_b.enterprise_value_mm / 1000]
    bars = ax.bar(labels, evs, color=colors, width=0.5)
    ax.set_title("Enterprise Value ($B)")
    ax.set_ylabel("$ Billions")
    for bar, val in zip(bars, evs):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.05,
                f"${val:.1f}B", ha="center", va="bottom", fontweight="bold")
    ax.set_ylim(0, max(evs) * 1.2)

    # --- Chart 2: Revenue ---
    ax = axes[0, 1]
    revs = [deal_a.revenue_mm, deal_b.revenue_mm]
    rev_labels_short = ["Net Rev\n(Downtown)", "Combined Net Rev\n(Playlist+EGYM)"]
    bars = ax.bar(rev_labels_short, revs, color=colors, width=0.5)
    ax.set_title("Net Revenue ($M)")
    ax.set_ylabel("$ Millions")
    for bar, val in zip(bars, revs):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 5,
                f"${val:.0f}M", ha="center", va="bottom", fontweight="bold")
    ax.set_ylim(0, max(revs) * 1.25)

    # --- Chart 3: EV / Revenue Multiple ---
    ax = axes[1, 0]
    ev_revs = [deal_a.ev_revenue, deal_b.ev_revenue]
    bars = ax.bar(labels, ev_revs, color=colors, width=0.5)
    ax.set_title("EV / Net Revenue Multiple")
    ax.set_ylabel("Multiple (x)")
    ax.axhline(y=5, color="gray", linestyle="--", linewidth=0.8, label="5x reference")
    for bar, val in zip(bars, ev_revs):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.1,
                f"{val:.1f}x", ha="center", va="bottom", fontweight="bold")
    ax.set_ylim(0, max(ev_revs) * 1.25)
    ax.legend(fontsize=8)

    # --- Chart 4: EV / EBITDA (Downtown only; Playlist/EGYM N/A) ---
    ax = axes[1, 1]
    ebitda_vals = [
        deal_a.ev_reported_ebitda if deal_a.ev_reported_ebitda else 0,
        deal_b.ev_reported_ebitda if deal_b.ev_reported_ebitda else 0,
    ]
    bar_labels = [labels[0], f"{labels[1]}\n(N/A — not disclosed)"]
    bars = ax.bar(bar_labels, ebitda_vals, color=colors, width=0.5)
    ax.set_title("EV / Reported EBITDA Multiple")
    ax.set_ylabel("Multiple (x)")
    ax.axhline(y=10, color="gray", linestyle="--", linewidth=0.8, label="10x reference (services norm)")
    for bar, val, orig in zip(bars, ebitda_vals, [deal_a.ev_reported_ebitda, deal_b.ev_reported_ebitda]):
        label = f"{val:.1f}x" if orig else "N/A"
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.1,
                label, ha="center", va="bottom", fontweight="bold")
    ax.set_ylim(0, max(ebitda_vals + [12]) * 1.25)
    ax.legend(fontsize=8)

    plt.tight_layout()


def plot_comparable_transactions(deal_a: Deal, deal_b: Deal):
    """Bar chart of EV/EBITDA multiples across comparable transactions."""
    if not HAS_MATPLOTLIB:
        return

    # Collect comps that have an EV/EBITDA figure, tagging which deal they belong to
    comps_a = [(ct.name, ct.ev_ebitda, "#4C9BE8") for ct in deal_a.comparable_transactions if ct.ev_ebitda]
    comps_b = [(ct.name, ct.ev_ebitda, "#E87B4C") for ct in deal_b.comparable_transactions if ct.ev_ebitda]

    # Include the deals themselves as the highlighted bars
    deal_bars = [
        (f"{deal_a.name.split('/')[0].strip()}\n(THIS DEAL)", deal_a.ev_reported_ebitda, "#1A5C9E"),
    ]

    all_bars = comps_a + comps_b + deal_bars
    if not all_bars:
        return

    names = [b[0] for b in all_bars]
    values = [b[1] for b in all_bars]
    colors = [b[2] for b in all_bars]

    # Wrap long names for readability
    wrapped = []
    for n in names:
        words = n.split()
        mid = len(words) // 2
        wrapped.append(" ".join(words[:mid]) + "\n" + " ".join(words[mid:]) if len(words) > 3 else n)

    fig, ax = plt.subplots(figsize=(11, 6))
    bars = ax.bar(wrapped, values, color=colors, width=0.55, edgecolor="white", linewidth=0.8)

    ax.set_title("EV / EBITDA: Downtown Deal vs. Comparable Transactions", fontsize=13, fontweight="bold")
    ax.set_ylabel("EV / EBITDA Multiple (x)")
    ax.axhline(y=10, color="gray", linestyle="--", linewidth=1, label="~10x (services norm)")
    ax.axhline(y=16, color="#999", linestyle=":", linewidth=1, label="~16x (music catalog norm)")

    for bar, val in zip(bars, values):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.2,
                f"{val:.1f}x", ha="center", va="bottom", fontsize=9, fontweight="bold")

    legend_patches = [
        mpatches.Patch(color="#4C9BE8", label=f"Comps: {deal_a.name.split('/')[0].strip()}"),
        mpatches.Patch(color="#E87B4C", label=f"Comps: {deal_b.name.split('+')[0].strip()}"),
        mpatches.Patch(color="#1A5C9E", label="Downtown deal (actual)"),
    ]
    ax.legend(handles=legend_patches, fontsize=8)
    ax.set_ylim(0, max(values) * 1.25)
    plt.tight_layout()
    plt.show()


def plot_sensitivity_table(deal: Deal, ebitda_scenarios: list, ev_scenarios: list):
    """
    Render a colour-coded sensitivity table: rows = EBITDA assumptions,
    cols = EV assumptions, cells = implied EV/EBITDA multiple.
    """
    if not HAS_MATPLOTLIB:
        return

    data = np.array([[ev / eb for ev in ev_scenarios] for eb in ebitda_scenarios])

    fig, ax = plt.subplots(figsize=(10, 4))
    ax.axis("off")

    col_labels = [f"EV ${ev}M" for ev in ev_scenarios]
    row_labels = [f"EBITDA ${eb}M" for eb in ebitda_scenarios]

    # Colour cells: green = below services norm (≤12x), yellow = elevated, red = high
    cell_colors = []
    for row in data:
        row_colors = []
        for val in row:
            if val <= 12:
                row_colors.append("#c6efce")   # green
            elif val <= 17:
                row_colors.append("#ffeb9c")   # yellow
            else:
                row_colors.append("#ffc7ce")   # red
        cell_colors.append(row_colors)

    cell_text = [[f"{val:.1f}x" for val in row] for row in data]

    tbl = ax.table(
        cellText=cell_text,
        rowLabels=row_labels,
        colLabels=col_labels,
        cellColours=cell_colors,
        loc="center",
        cellLoc="center",
    )
    tbl.auto_set_font_size(False)
    tbl.set_fontsize(10)
    tbl.scale(1.3, 1.6)

    # Bold the column/row corresponding to the deal's actual values
    actual_ev_idx = ev_scenarios.index(deal.enterprise_value_mm) if deal.enterprise_value_mm in ev_scenarios else None
    actual_eb_idx = ebitda_scenarios.index(deal.reported_ebitda_mm) if deal.reported_ebitda_mm in ebitda_scenarios else None
    if actual_ev_idx is not None and actual_eb_idx is not None:
        tbl[actual_eb_idx + 1, actual_ev_idx].set_text_props(fontweight="bold")

    ax.set_title(
        f"Sensitivity: {deal.name.split('/')[0].strip()} — EV/EBITDA at Varying Assumptions\n"
        "Green ≤ 12x (services norm) | Yellow 12–17x | Red > 17x",
        fontsize=11, fontweight="bold", pad=20,
    )

    # Add a note about actual values
    note = (f"Actual deal: EV = ${deal.enterprise_value_mm:.0f}M, "
            f"Reported EBITDA = ${deal.reported_ebitda_mm:.0f}M → "
            f"{deal.ev_reported_ebitda:.1f}x")
    fig.text(0.5, 0.02, note, ha="center", fontsize=9, style="italic", color="#333")

    plt.tight_layout()
    plt.show()


